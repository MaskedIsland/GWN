def drop_feature(batch_x, drop_prob):
    node_num = batch_x.shape[2]
    node_feat = batch_x.shape[3]
    drop_mask = torch.empty((node_num, node_feat), dtype=torch.float64, device=batch_x.device).uniform_(0, 1) < drop_prob
    drop_mask[:,1:6] = False
    batch_x = batch_x.clone()
    batch_x[:, :, drop_mask] = 0

    return batch_x

def sim(z1: torch.Tensor, z2: torch.Tensor):
    z1 = F.normalize(z1)
    z2 = F.normalize(z2)
    return torch.mm(z1, z2.t())

def semi_loss(z1: torch.Tensor, z2: torch.Tensor):
    f = lambda x: torch.exp(x / 0.5)
    refl_sim = f(sim(z1, z1))
    between_sim = f(sim(z1, z2))

    return -torch.log(between_sim.diag() / (refl_sim.sum(1) + between_sim.sum(1) - refl_sim.diag()))

def cl_loss(z1: torch.Tensor, z2: torch.Tensor, mean: bool=True):
    l1 = semi_loss(z1, z2)
    l2 = semi_loss(z2, z1)
    ret = (l1 + l2) * 0.5
    ret = ret.mean() if mean else ret.sum()
    return ret

